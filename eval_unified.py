# BM25
# ─────────────────────────────────────────────────────────────────────────────
def build_bm25(docs):
    return BM25Okapi([d.lower().split() for d in docs])
 
def bm25_retrieve(bm25, query, top_k=10):
    scores = bm25.get_scores(query.lower().split())
    return sorted(range(len(scores)), key=lambda i: scores[i], reverse=True)[:top_k]
 
 
